# ArrayCommands19.py
# This program demonstrates how to sort an array of
# numbers in descending order.  It is done in 2 steps.
# First you <sort> the list; then you <reverse> it.


gpas = [3.9,2.75,2.1,1.65,4.0,3.25,2.45,0.95,3.88,3.75]

print()
print(gpas)

gpas.sort()

print()
print(gpas)

gpas.reverse()

print()
print(gpas)
 