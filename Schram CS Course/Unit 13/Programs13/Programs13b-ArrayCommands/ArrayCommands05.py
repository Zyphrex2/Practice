# This program demonstrates the <index> function 
# which will return the "index" of a particular 
# item in the array.


names = ["John","Greg","Maria","Heidi","Diana","Kisha"]

print()
print(names)

print("\nDiana is located at index",names.index("Diana"))

print("\nJohn is located at index",names.index("John"))


 