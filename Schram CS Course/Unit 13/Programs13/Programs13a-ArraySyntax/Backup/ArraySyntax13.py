# ArraySyntax13.py
# This program demonstrates how to display a
# "slice" of an array using the [start:stop] 
# syntax.  The array is displayed starting on
# first index and stopping just BEFORE the second.


list = [100,101,102,103,104,105,106,107,108,109]

print()
print(list)

print()
print(list[3:8])
