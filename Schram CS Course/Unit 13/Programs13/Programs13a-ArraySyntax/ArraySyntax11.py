# ArraySyntax11.py
# This program demonstrates that 2 arrays 
# can be combined to create a third, larger 
# array using the overloaded + operator.
# This is very similar to "string concatenation".


listA = [11,22,33,44,55]
listB = [66,77,88,99]

listC = listA + listB

print()
print(listA)
print(listB)
print(listC)