# MoreErrors06.py
# This program demonstrates a "Logic Error".
# These can be the most frustrating errors 
# because no error is actually detected.
# The program executes just fine.
# It just does not do what you want it to do.
# In this case the average is wrong because
# "Order of Operations" was not considered.


num1 =  70
num2 =  80
num3 =  90
num4 = 100

average = num1 + num2 + num3 + num4 / 4

print()
print("Average:",average)
