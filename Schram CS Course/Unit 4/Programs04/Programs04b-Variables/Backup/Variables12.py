# Variables12.py
# This program demonstrates "String Concatenation"
# which is joining together 2 or more strings.

firstName = "Suzy"
lastName = "Snodgrass"

fullName1 = firstName + lastName
fullName2 = firstName + " " + lastName
fullName3 = lastName + ", " + firstName

print()
print(fullName1)
print(fullName2)
print(fullName3)
