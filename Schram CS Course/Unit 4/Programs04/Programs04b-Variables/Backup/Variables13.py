# Addition vs. Concatenation
# Use <+> with numbers and you get addition.
# Use <+> with strings and you get concatenation.
# Since <+> can do 2 different things, it is 
# called an "Overloaded Operator".


number1 = 100 + 200
number2 = "100" + "200"

print()
print(number1)
print(number2)
 
 