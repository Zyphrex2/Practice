# Variables17.py
# The program demonstrates the <type> command which
# will give you the data type of a particular value.
# NOTE: Python does not distinguish between string
# and character values.  Both are <str>.
# ALSO: Another word for a "real number" is a
# "floating point number".  When scientific notation
# is used, the resulting value is a <float>.


a = 7
pi = 3.141592653589793
name = "John Smith"
middleInitial = 'Q'
passing = True
mole = 6.02e23

print()
print(type(a))
print(type(pi))
print(type(name))
print(type(middleInitial))
print(type(passing))
print(type(mole))

