# Variables04.py
# This program demonstrates how to print the 
# values of multiple variables on one line.
# By default, these values are separated by
# a single space, but this can be changed
# by using <sep> at the end of <print>.


x = 50
y = 75
z = 100

print()
print(x,y)
print(x,y,z)
print()
print(x,y,sep="")
print(x,y,z,sep="<:>")
print(x,y,z,sep="\t\t")

