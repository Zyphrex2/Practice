# TurtleGraphics15.py
# This program demonstrates several colors.

from turtle import *

setup(800,600)

backward(300)
width(300)
color("red")
forward(100)
color("orange")
forward(100)
color("yellow")
forward(100)
color("green")
forward(100)
color("light blue")
forward(100)
color("blue")
forward(100)
color("dark blue")
forward(100)
color("purple")
forward(100)

update()
done()
