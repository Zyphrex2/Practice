# TurtleGraphics12.py
# This program demonstrates that different 
# lines can have different widths -- which 
# can be quite thick.


from turtle import *

setup(800,600)

width(50)
forward(200)
right(90)
width(100)
forward(200)
right(90)
width(50)
forward(200)
right(90)
width(100)
forward(200)
right(90)

update()
done()
