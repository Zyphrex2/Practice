# mathLibrary09.py
# This program demonstrates the <ceil> function of  
# the <math> library which always "rounds up".


from math import *

print()
print("5.999 rounded up is",ceil(5.999))
print("5.501 rounded up is",ceil(5.501))
print("5.5   rounded up is",ceil(5.5))
print("5.499 rounded up is",ceil(5.499))
print("5.001 rounded up is",ceil(5.001))
print("-5.5  rounded up is",ceil(-5.5))
