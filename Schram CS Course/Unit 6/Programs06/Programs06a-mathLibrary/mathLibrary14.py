# mathLibrary14.py
# The program demonstrates the <factorial> 
# function of the <math> library which returns 
# the mathematical factorial of its argument.

# Example: factorial(n) = n * (n-1) * ... * 2 * 1

# NOTE: This program also demonstrates that in 
#       Python integer values can be VERY large.


from math import *

print()
print("5! is",factorial(5))
print()
print("10! is",factorial(10))
print()
print("40! is",factorial(40))
