# GraphicsLibrary06.py
# This program demonstrates the <drawCircle> 
# procedure of the <Graphics> library.  
# Circles are drawn from their center (x,y) 
# with a particular radius with
# <drawCircle(x,y,radius)>.


from Graphics import *

beginGrfx(1300,700)

drawCircle(200,200,100)
drawCircle(1300,0,300)
drawCircle(650,350,100)
drawCircle(650,350,200)
drawCircle(300,550,80)
drawCircle(1100,500,120)

endGrfx()
