# StringOperators04.py
# This program demonstrates how to access 
# a piece of a string.
# This is usually called a "sub-string".
# It is accomplished via "String Slicing"
# which is very similar to "Array Slicing".


s = "Racecar"

print()
print(s[0:4])
print(s[1:4])
print(s[2:4])
print(s[2:6])
print(s[3:6])
print(s[4:7])
print(s[4:])
print(s[:4])
