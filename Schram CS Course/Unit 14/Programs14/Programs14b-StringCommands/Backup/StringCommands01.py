# StringCommands01.py
# This program demonstrates the <len> command. 
# In the same way that <len> will tell you 
# how many items are in an array, it will also 
# tell you how many characters are in a string.


s1 = "Argentine"
s2 = "Tango"
s3 = s1 + " " + s2                  

print()
print(s1,"has",len(s1),"characters.")
print(s2,"has",len(s2),"characters.")
print(s3,"has",len(s3),"characters.")
