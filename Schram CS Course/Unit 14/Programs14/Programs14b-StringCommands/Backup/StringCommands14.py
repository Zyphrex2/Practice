# This program demonstrates how to use <int>, <float> and 
# <bool> to convert string values to integers, real numbers 
# (a.k.a. floating point numbers) and Boolean values.


n1 = int("1000")
n2 = float("234.432")
n3 = n1 + n2
b1 = bool("True") 

print()
print("n1:",n1)
print("n2:",n2)
print("n3:",n3)

if b1:
   print("b1:",b1)
