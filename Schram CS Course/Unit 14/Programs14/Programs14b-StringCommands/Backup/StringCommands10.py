# StringCommands10.py
# This program demonstrates the <upper> and <lower> commands
# which convert string to all UPPERCASE or all lowercase.
# NOTE: Like the <replace> command, <upper> and <lower> 
#       do not alter the original string in ANY way.


s1 = "racecar"
s2 = "RaCeCaR"
s3 = ">RACECAR-100<"

print("\nUppercased Strings:")
print(s1.upper())
print(s2.upper())
print(s3.upper())

print("\nLowercased Strings:")
print(s1.lower())
print(s2.lower())
print(s3.lower())

print("\nOriginal Strings:")
print(s1)
print(s2)
print(s3)
