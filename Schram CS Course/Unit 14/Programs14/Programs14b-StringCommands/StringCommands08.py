# StringCommands08.py
# This program demonstrates the <replace> command 
# which can replace characters or substrings.
# NOTE: The <replace> command returns an altered 
#       COPY of the original string. It does not 
#       alter the original string in ANY way.


s1 = "racecar"
s2 = s1.replace('r','l')
s3 = s1.replace("ace","eally awesome ")

print()
print(s1)
print(s2)
print(s1)
print(s3)
 