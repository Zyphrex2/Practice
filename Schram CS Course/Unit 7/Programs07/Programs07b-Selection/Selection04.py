# This program demonstrates two-way selection 
# with <if..else>.  Run the program twice:  
# First with 1200, then with 1000.


sat = eval(input("Enter SAT score  -->  "))
if sat >= 1100:
   print("You are admitted.")
else:
   print("You are not admitted.")
   
if sat == 1600:
   print("Perfect!  You are a genius!")   
   