# Boolean06.py
# This program assigns the values of 4 different
# expressions to 4 different variables.  
# The intent is to show that storing the values
# of Boolean expressions is no different than
# storing the values of any other expressions.


x = 23 + 45                 # Integer expression

y = 6.33 * 6.31 * 6.23      # Real Number expression

name = "John " + "Smith"    # String expression

average = 85
passing = average >= 70     # Boolean expression

print()
print("Integer value:    ",x)
print("Real Number value:",y)
print("String value:     ",name)
print("Boolean value:    ",passing)
