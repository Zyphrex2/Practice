# Ranges01.py
# This program determines if an age is between 16 and 90.
# The range is defined using a compound condition with <and>.
# NOTE: This range logic works in any programming language.


print()
age  = eval(input("How old are you?   -->  "))
print()

if age >= 16 and age <= 90:
   print("You are the proper age to drive.")
else:
   print("You are not the proper age to drive.")
  